-- =====================================================
-- 7. Function to auto-update thread.updated_at
-- =====================================================
CREATE OR REPLACE FUNCTION update_thread_timestamp()
RETURNS TRIGGER AS $$
BEGIN
  UPDATE journal_threads
  SET 
    updated_at = NOW(),
    last_message_at = NEW.created_at
  WHERE id = NEW.thread_id;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Trigger when new message is inserted
DROP TRIGGER IF EXISTS trigger_update_thread_timestamp ON journal_messages;
CREATE TRIGGER trigger_update_thread_timestamp
  AFTER INSERT ON journal_messages
  FOR EACH ROW
  WHEN (NEW.thread_id IS NOT NULL)
  EXECUTE FUNCTION update_thread_timestamp();

-- =====================================================
-- 8. Helper function to get or create active thread
-- =====================================================
CREATE OR REPLACE FUNCTION get_or_create_active_thread(p_user_id UUID)
RETURNS UUID AS $$
DECLARE
  v_thread_id UUID;
BEGIN
  -- Try to find an active thread from today
  SELECT id INTO v_thread_id
  FROM journal_threads
  WHERE user_id = p_user_id
    AND status = 'active'
    AND DATE(created_at) = CURRENT_DATE
  ORDER BY last_message_at DESC
  LIMIT 1;

  -- If no thread exists, create one
  IF v_thread_id IS NULL THEN
    INSERT INTO journal_threads (user_id, title)
    VALUES (p_user_id, 'Journal - ' || TO_CHAR(NOW(), 'YYYY-MM-DD HH24:MI'))
    RETURNING id INTO v_thread_id;
  END IF;

  RETURN v_thread_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;
