-- Journal Schema for RYTM
-- Run this in Supabase SQL Editor

-- =====================================================
-- 1. Drop old tables if migrating (CAREFUL!)
-- =====================================================
-- Uncomment these if you want to start fresh:
DROP TABLE IF EXISTS journal_entries CASCADE;
DROP TABLE IF EXISTS journal_messages CASCADE;
DROP TABLE IF EXISTS journal_threads CASCADE;

-- =====================================================
-- 2. Create journal_threads table
-- =====================================================
CREATE TABLE IF NOT EXISTS journal_threads (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  title TEXT, -- Can be auto-generated by LLM later
  status TEXT DEFAULT 'active' CHECK (status IN ('active', 'completed', 'archived')),
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  last_message_at TIMESTAMPTZ DEFAULT NOW()
);

-- =====================================================
-- 3. Create journal_messages table
-- =====================================================
CREATE TABLE IF NOT EXISTS journal_messages (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  thread_id UUID REFERENCES journal_threads(id) ON DELETE CASCADE, -- NULL for Free mode
  mode TEXT NOT NULL CHECK (mode IN ('free', 'guided')),
  role TEXT NOT NULL CHECK (role IN ('user', 'assistant')),
  content TEXT NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- =====================================================
-- 4. Create indexes for performance
-- =====================================================
CREATE INDEX IF NOT EXISTS idx_threads_user_updated 
  ON journal_threads(user_id, updated_at DESC);

CREATE INDEX IF NOT EXISTS idx_threads_user_last_message 
  ON journal_threads(user_id, last_message_at DESC);

CREATE INDEX IF NOT EXISTS idx_messages_user_date 
  ON journal_messages(user_id, created_at DESC);

CREATE INDEX IF NOT EXISTS idx_messages_thread_date 
  ON journal_messages(thread_id, created_at);

CREATE INDEX IF NOT EXISTS idx_messages_mode 
  ON journal_messages(user_id, mode, created_at DESC);

-- =====================================================
-- 5. RLS Policies for journal_threads
-- =====================================================
ALTER TABLE journal_threads ENABLE ROW LEVEL SECURITY;

-- Users can view their own threads
CREATE POLICY "Users can view own threads"
  ON journal_threads FOR SELECT
  TO authenticated
  USING (auth.uid() = user_id);

-- Users can create threads
CREATE POLICY "Users can create threads"
  ON journal_threads FOR INSERT
  TO authenticated
  WITH CHECK (auth.uid() = user_id);

-- Users can update their own threads
CREATE POLICY "Users can update own threads"
  ON journal_threads FOR UPDATE
  TO authenticated
  USING (auth.uid() = user_id)
  WITH CHECK (auth.uid() = user_id);

-- Users can delete their own threads
CREATE POLICY "Users can delete own threads"
  ON journal_threads FOR DELETE
  TO authenticated
  USING (auth.uid() = user_id);

-- =====================================================
-- 6. RLS Policies for journal_messages
-- =====================================================
ALTER TABLE journal_messages ENABLE ROW LEVEL SECURITY;

-- Users can view their own messages
CREATE POLICY "Users can view own messages"
